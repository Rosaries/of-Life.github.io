FINAL PRAYERS OF THE ROSARY <br><br>

<u><b> Hail Holy Queen </b></u> <br>
Hail, Holy Queen, mother of mercy, hail our life, our sweetness, and our hope.  To thee do we cry poor banished children of Eve, to thee do we send up our sighs, mourning and weeping in this valley of tears.  Turn then Most Gracious Advocate, thine eyes of mercy toward us, and after this, our exile, show unto us the blessed fruit of thy womb, Jesus.  O Clement, O Loving, O Sweet Virgin Mary, pray for us, O Holy Mother of God, that we may be worthy of the promises of Christ.  Amen. 
<br><br>
<u><b> St. Michael the Archangel Prayer </b></u><br>
St. Michael the Archangel, defend us in battle.  Be our protection against the wicked snares of the devil.  May God rebuke him, we humbly pray, and do thou, O Prince of the heavenly hosts, by the power of God, cast into hell Satan and all the evil the spirits who prowl around the world seeking the ruin of souls.  Amen.  
<br><br>
