  <u><b>Consecration through St. Joseph </b></u><br>

  O Glorious St. Joseph, like Jesus, I totally entrust myself to you.  As you formed your son to live the Gospel of Life, so fully form me in the image and likeness of the same Christ, our Lord.  Amen.  St. Joseph, increase our faith. 
  <br><br>

  <u><b>Fatima Pardon Prayers </b></u><br>

  My God, I believe, I adore, I hope and I love Thee. I beg pardon for all those that do not believe, do not adore, do not hope and do not love Thee. Amen.
  <br><br>

  <u><b>Angel of Fatima Prayer </b></u><br>

  O Most Holy Trinity, Father, Son and Holy Spirit, I profoundly adore Thee, and I offer Thee the most precious Body, Blood, Soul and Divinity of our Lord Jesus Christ present in all the tabernacles throughout the whole world, in reparation for the outrages, sacrileges and indifferences made against Thee. By the infinite merits of the Sacred Heart of Jesus and the Immaculate Heart of Mary, I beg of Thee for the conversion of poor sinners. Amen.
  <br><br>
  <u><b>Fatima Sacrificial Offering to God </b></u><br>

  O Jesus, it is for the love of Thee, in reparation for the offenses committed against the Immaculate Heart of Mary, and for the conversion of poor sinners, that I endure this hardship and offer this sacrifice.  Amen.
  <br><br>
  <u><b>New Pentecost Prayer </b></u><br>

  Come, Holy Spirit!  Confirm the Word of God with signs and wonders that unbelievers may believe, and the lukewarm may grow zealous, and the zealous may be fully consecrated to the divine will of God our Father in the Immaculate Heart of Mary, our Mother.  Amen!
