  OPTIONAL DEVOTIONAL PRAYERS <br>

  <u><b>Shield of Faith Prayer</b></u><br>

  Jesus, I trust in you! <br>
  You are all powerful and you love me.<br>
  You are faithful and you are with me.<br>
  You are merciful and you have the power <br>
